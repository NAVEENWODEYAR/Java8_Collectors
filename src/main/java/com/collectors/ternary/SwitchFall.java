public class SwitchFall {
    public static void main(String[] args) {
        int value = 2;
        switch (value) {
            case 1:
                System.out.println("One");
            case 2:
                System.out.println("Two");
            case 3:
                System.out.println("Three");
            default:
                System.out.println("Default");
        }
    }
}
